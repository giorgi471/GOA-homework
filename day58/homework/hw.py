#  მეორდება 2-ზე მეტჯერ, გამოიყენეთ remove() ფუნქცია და while ციკლი.

A1 = [1, "a", 2, 1, "a", True, 2, 3, False, True]

i = 0
while i < len(A1):
    count = A1.count(A1[i])

    if count > 1:
        A1.remove(A1[i])
    else:
        i += 1

print(A1)




