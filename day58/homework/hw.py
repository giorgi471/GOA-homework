nums = [1, 2, 3, 4, 5, 6]

even_odd_index = []
odd_even_index = []

for i in range(len(nums)):
    if nums[i] % 2 == 0 and i % 2 == 1:
        even_odd_index.append(nums[i])
    if nums[i] % 2 == 1 and i % 2 == 0:
        odd_even_index.append(nums[i])

print(even_odd_index)
print(odd_even_index)





